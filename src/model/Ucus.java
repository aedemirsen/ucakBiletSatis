public class Ucus {
    
    private String ucusAdi;
    private String ucusTarih;
    private String kalkisSaat;
    private String inisSaat;
    private String kalkisYeri;
    private String inisYeri;

    public String getUcusAdi() {
        return ucusAdi;
    }

    public void setUcusAdi(String ucusAdi) {
        this.ucusAdi = ucusAdi;
    }

    public String getUcusTarih() {
        return ucusTarih;
    }

    public void setUcusTarih(String ucusTarih) {
        this.ucusTarih = ucusTarih;
    }

    public String getKalkisSaat() {
        return kalkisSaat;
    }

    public void setKalkisSaat(String kalkisSaat) {
        this.kalkisSaat = kalkisSaat;
    }

    public String getInisSaat() {
        return inisSaat;
    }

    public void setInisSaat(String inisSaat) {
        this.inisSaat = inisSaat;
    }

    public String getKalkisYeri() {
        return kalkisYeri;
    }

    public void setKalkisYeri(String kalkisYeri) {
        this.kalkisYeri = kalkisYeri;
    }

    public String getInisYeri() {
        return inisYeri;
    }

    public void setInisYeri(String inisYeri) {
        this.inisYeri = inisYeri;
    }
    
}
